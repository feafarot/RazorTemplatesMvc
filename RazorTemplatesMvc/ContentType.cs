namespace RazorTemplatesMvc
{
    /// <summary>
    /// Http content types.
    /// </summary>
    public enum ContentType
    {
        /// <summary>
        /// CSS content type will be translated to <value>text/css</value> type attribute value.
        /// </summary>
        Css = 0,
        
        /// <summary>
        /// JavaScript content type will be translated to <value>text/javascript</value> type attribute value.
        /// </summary>
        Js = 1
    }
}
